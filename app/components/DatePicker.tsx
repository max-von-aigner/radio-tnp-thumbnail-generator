"use client";

import * as React from "react";
import { CalendarIcon } from "@radix-ui/react-icons";
import { format } from "date-fns";
import { cn } from "@/lib/utils";
import { Button } from "@/components/ui/button";
import { Calendar } from "@/components/ui/calendar";
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover";
import { Label } from "@/components/ui/label";

interface DatePickerProps {
  setDateExternal: (date: Date | undefined) => void;
}

// Add props to DatePicker for lifting state up
export function DatePicker({ setDateExternal }: DatePickerProps) {
  const [date, setDate] = React.useState<Date>(new Date());

  React.useEffect(() => {
    setDateExternal(date); // Lift state up whenever date changes
  }, [date, setDateExternal]);
  return (
    <div className="flex flex-col">
      <Label htmlFor="date" className="pb-1">
        Date
      </Label>
      <Popover>
        <PopoverTrigger asChild>
          <Button
            variant={"outline"}
            className={cn(
              "w-[240px] justify-start text-left font-normal",
              !date && "text-muted-foreground"
            )}
          >
            <CalendarIcon className="mr-2 h-4 w-4" />
            {date ? format(date, "PPP") : <span>Pick a date</span>}
          </Button>
        </PopoverTrigger>
        <PopoverContent className="w-auto p-0" align="start">
          <Calendar
            mode="single"
            selected={date}
            onSelect={(day: Date | undefined) => setDate(day || new Date())}
            initialFocus
          />
        </PopoverContent>
      </Popover>
    </div>
  );
}
